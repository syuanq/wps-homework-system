def retrieve_knowledge(task_id, level_key, failed_skills=None):
    """
    根据任务ID、等级和未掌握技能检索知识库内容，返回格式化文本用于注入AI prompt。

    Args:
        task_id: 任务ID（如 task_4_3）
        level_key: 等级标识（excellent/good/pass/fail）
        failed_skills: 未掌握的技能名称列表

    Returns:
        str: 格式化的知识文本
    """
    # 获取任务信息
    task_info = ALL_TASKS.get(task_id, {})
    module_group = task_info.get('module_group', '')

    # 获取对应模块的知识（优先从JSON文件读取）
    kb = get_knowledge_base()
    module_kb = kb.get(module_group, {})
    if not module_kb:
        return ""

    # 构建知识文本
    parts = []

    # 1. 模块核心概念
    concepts = module_kb.get('concepts', [])
    if concepts:
        parts.append(f"【{module_kb['module_name']}核心概念】")
        for c in concepts:
            parts.append(f"  - {c}")

    # 2. 任务相关知识
    task_kb = module_kb.get('task_knowledge', {}).get(task_id, {})
    if task_kb:
        parts.append(f"\n【教材章节：{task_kb.get('chapter', '')}】")
        parts.append("知识点要求：")
        for kp in task_kb.get('key_points', []):
            parts.append(f"  - {kp}")

    # 3. 根据未掌握技能检索相关操作
    if failed_skills:
        all_operations = module_kb.get('operations', [])
        relevant_ops = []
        for skill in failed_skills:
            for op in all_operations:
                # 模糊匹配：技能名包含在操作名中，或操作名包含技能关键词
                skill_keywords = skill.replace('设置', '').replace('操作', '')
                if (skill_keywords in op['name'] or
                    any(kw in op['name'] for kw in skill_keywords.split('/'))):
                    if op not in relevant_ops:
                        relevant_ops.append(op)

        if relevant_ops:
            parts.append(f"\n【相关操作步骤（针对未掌握技能）】")
            for op in relevant_ops[:3]:  # 最多3个操作
                parts.append(f"\n{op['name']}：")
                for step in op['steps']:
                    parts.append(f"  {step}")

    # 4. 常见错误提示
    common_mistakes = module_kb.get('common_mistakes', [])
    if common_mistakes:
        parts.append(f"\n【学生常见错误】")
        for m in common_mistakes[:5]:
            parts.append(f"  ⚠ {m}")

    # 5. 根据等级给出教学建议
    level_tips = {
        "excellent": "学生基础扎实，建议设计拓展性任务，如综合运用多种高级功能、跨模块整合操作。",
        "good": "学生基本掌握，建议设计巩固提升任务，重点练习未掌握的技能，适当增加难度。",
        "pass": "学生初步掌握，建议设计针对性练习，每个步骤要详细，重点加强薄弱环节。",
        "fail": "学生基础薄弱，建议从最基础的操作开始，步骤要非常详细，配合操作截图。",
    }
    parts.append(f"\n【教学策略建议（{level_key}等级）】")
    parts.append(f"  {level_tips.get(level_key, '')}")

    return "\n".join(parts)
